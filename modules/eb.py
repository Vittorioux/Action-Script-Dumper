# Valid argument types.

# If the arguments list is empty, no arguments are assumed.
# 'u_8'              Unsigned byte, to be represented in decimal.
# 'u_16'             Unsigned 16-bit word, to be represented in decimal.
# 'hex_8'            Unsigned byte, to be represented in hexadecimal.
# 'hex_16'           Unsigned 16-bit word, to be represented in hexadecimal.
# 'hex_24'           Unsigned 24-bit address.
# 'hex_32'           Unsigned 32-bit address.
# 's_8'              Signed byte, to be represented in decimal.
# 's_16'             Signed 16-bit word, to be represented in decimal.
# 'l_16'             16-bit word to be replaced by a label if available (hex_16 otherwise), created when necessary.
# 'l_24'             24-bit address to be replaced by a label if available (hex_24 otherwise), created when necessary.
# 'var'              Unsigned byte signaling a VAR. The VAR number will get appended after the main opcode name/terminator if present.
# 'op'               Unsigned byte signaling an operation ('and', 'or', 'add' or 'xor'). The operation name will get appended after the main opcode name.
# 'multi'            When this argument is the FIRST one in the list, a byte will be read, the value of said byte will determine how many of the second argument to read afterwards.
# 'asm_arg'          When this argument IS PRESENT, three bytes will be read; then, the asmcall list is revised for named routines or routines using arguments.

# Valid newline behavior.

